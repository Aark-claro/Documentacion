USE backlog_nacional;

CREATE TABLE IF NOT EXISTS backlog_nacional (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    TIPO_TRABAJO VARCHAR(64),
    TIPO_BACKLOG VARCHAR(64),
    CUENTA VARCHAR(30),
    OT_LL VARCHAR(32),
    FECHA_CREADO DATE,
    HORA_CREADO VARCHAR(16),
    USUARIO_CREADOR VARCHAR(64),
    NODO VARCHAR(64),
    RAZON VARCHAR(128),
    SEGMENTO VARCHAR(64),
    CLASE VARCHAR(64),
    CONVENIENCIA VARCHAR(16),
    SUS_NOMBRE VARCHAR(128),
    SUS_APELLIDO VARCHAR(128),
    TELEFONO1 VARCHAR(32),
    TELEFONO2 VARCHAR(32),
    TELEFONO3 VARCHAR(32),
    DIRECCION_CARRERA VARCHAR(128),
    DIRECCION_CALLE VARCHAR(128),
    DIRECCION_APT VARCHAR(64),
    CEDULA_VENDEDOR VARCHAR(32),
    TARIFA VARCHAR(64),
    DIRECCION_CALLE_OLD VARCHAR(128),
    DIRECCION_APT_OLD VARCHAR(64),
    ANTIGUEDAD_DIGITACION INT,
    REGION VARCHAR(64),
    AREA VARCHAR(64),
    RED VARCHAR(64),
    COMUNIDAD VARCHAR(128),
    ALIADO_ZONIFICADO VARCHAR(128),
    OPERA VARCHAR(64),
    FECHA_AGENDA_FUTURO DATETIME,
    FECHA_ULTIMA_VISITA DATETIME,
    ESTADO_VISITA VARCHAR(64),
    RAZON_NODONE VARCHAR(128),
    ANTIGUEDAD_ULTIMA_VISITA INT,
    DIVISION VARCHAR(64),
    REGIONAL VARCHAR(64),
    CODIGO_DX VARCHAR(64),
    ESTADO_ORDEN VARCHAR(64),
    WMTYPS VARCHAR(64),
    CUENTA_MATRIZ VARCHAR(30),
    TRONCAL VARCHAR(64),
    TRONCAL_P VARCHAR(64),
    COORDENADAS VARCHAR(128),
    NOTA_RR_1 TEXT,
    NOTA_RR_2 TEXT,
    descripcion TEXT,
    usuario_descripcion VARCHAR(50),
    fecha_descripcion TIMESTAMP,
    solicitud VARCHAR(100),
    motivo VARCHAR(100),
    fecha_reagendar VARCHAR(10),
    franja_horaria ENUM('AM','PM'),
    ESTADO ENUM('CANCELADO','COMPLETADO','GESTIONADA','INICIADO','NO COMPLETADO','PENDIENTE','SUSPENDIDO'),
    FECHA_HORA_CREADO DATETIME,
    INDEX idx_ot_ll (OT_LL),
    INDEX idx_region (REGION),
    INDEX idx_estado (ESTADO),
    INDEX idx_fecha_creado (FECHA_CREADO)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Tabla para última actualización
CREATE TABLE IF NOT EXISTS ultima_actualizacion (
    id INT PRIMARY KEY DEFAULT 1,
    fecha_descarga DATETIME,
    nombre_archivo VARCHAR(255)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
